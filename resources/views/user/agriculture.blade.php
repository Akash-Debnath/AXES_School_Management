                    <h2>COURSE DESCRIPTION</h2>
                    <p><strong>About Us</strong></p> 
					<p> The Department of Information and Communication Technology  (ICT) at the Rabindra Maitree University started its academic activities from July-December 2017 semester and currently it offers BSc Engineering program. Through the program, the department provides high quality education, planning, designing and deploying in both the theoretical and practical ways about information technology. Department aims to promote education, innovative research and create competent and skilled professionals in Information and Communication Technology.</p>

                        <strong>Vision of the Department </strong>

                        <p>To become global standard leading entity in preparing skilled personnel enriched with modern agricultural knowledge and technology.

                            .</P>
                        

                            <strong>Mission of the Department</strong>

                            <p>  The department will furnish students to be highly innovative, knowledgeable and skilled personnel in the field of agriculture by ensuring basic and advanced knowledge and technologies of agriculture to prepare as global competitor.   </p>
                        
