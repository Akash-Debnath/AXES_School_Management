                    <p><strong>About Us</strong></p> 
					<p> The Department of Computer Science and Engineering (CSE) at Rabindra Maitree University (RMU) is a place where most decorated thoughts are assembled for a higher purpose. The Department of CSE currently offers BSc Engineering in Computer Science. The analysis of computer-programming itself researches various aspects of the use of programming languages and complex systems, and human-computer interaction focuses on the challenges to create computers and computations useful, usable, and globally available to mankind. We think that all members of the community are catalysts of development and encourage them to escape from traditional learning and apply attitude to generate new knowledge and others to perform the same. Through the program the department ensures highly competitive environment, world-class facilities with hard working faculties that helps the students to be an enthusiastic leader the field computer related technology. </p>

