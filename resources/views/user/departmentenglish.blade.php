                        <h2>COURSE DESCRIPTION</h2>
                            <p ><strong>BACHELOR OF ARTS WITH HONOURS IN ENGLISH</strong></p>
                        <p class="western" align="center">&nbsp;</p>
                        <p></span><span style="color: #333333;">ability to communicate clearly, think independently, reason logically and analyze critically. </span></span></span></span></p>
                        <p></span><span style="color: #333333;"><strong>Vision of the program</strong></span></span></span></p>
                        <p><span style="color: #333333;">To develop the learner's ability to reach the level of literary expertise that nourishes humanitarian outlook of life.</span></span></span></p>
                        <p class="western" align="justify">&nbsp;</p>
                        <p><span style="color: #333333;"><strong>Mission of the program</strong></span></span></span></p>
                        <p><span style="color: #333333;">The program is designed to give the students a strong foundation of language and literature equipping them with professional skills to enhance the opportunity of employability. Alongside the study of texts in print, they will be introduced to the latest developments in digital media and new media storytelling. There is also a strong emphasis on honing the writing skills-creative writing, journalism, opening the door to an exciting range of career possibilities and further study.</span></span></span></p>
                        <p class="western" align="justify">&nbsp;</p>
                        <p><span style="color: #333333;"><strong>OBJECTIVES</strong></span><span style="font-family: Times New Roman, serif;">: </span></span></span></p>
                        <p><span style="color: #333333;">Under the program, the courses are designed to increase four categorical skills: </span></span></span></p>
                        <ul>
                        <ul>
                            <li><strong>Language Training:</strong><span style="font-family: Times New Roman, serif;"> To build up students’ listening, speaking, reading, and writing capabilities in English.</span></li>
                            </ul>
                        </ul>
                            <ul>
                            <ul>
                            <li><strong>Literature</strong><span style="font-family: Times New Roman, serif;">: To empower students with knowledge of multi-cultural literature and broaden their vision internationally.</span></li>
                            </ul>
                            </ul>
                                <ul>
                                <ul>
                                <li><strong>Linguistics</strong><span style="font-family: Times New Roman, serif;">: To introduce students to the structure, meaning and function of English language</span></li>
                                </ul>
                                </ul>
                                <ul>
                                <ul>
                                <li><strong>Applied English</strong><span style="font-family: Times New Roman, serif;">: To improve spoken and written communication skills as a foundation for pursuing secondary professional skills, so as to meet the demands of career development research and diversity in the present social milieu.</span></li>
                                </ul>
                                </ul>
                        <p class="western" align="justify">&nbsp;</p>
                        <p><span style="color: #333333;"><strong>The Language Training courses</strong>include four years of English Lab and three years of English Composition. The English Lab courses begin with basic daily conversations, gradually move on to more complicated skills such as description, oral presentation, debate, and finally lead toward advanced skills such as academic communication and critical discussion of social issues. The English Composition courses start with grammar and sentence structure and then provide guided training on paragraph writing, leading toward organizational and rhetorical composition. The courses also incorporate test materials from GEPT or TOEFL and emphasize writing in different genres. </span></p>
                        <p class="western" align="justify">&nbsp;</p>
                        <p><span style="color: #333333;"><strong>The Literature courses </strong>>include required survey courses such as Introduction to Western literature, English literature, American literature, and European literatures, and elective courses like drama, fiction, English poetry, and prose, Bible as literature, children’s literature, comparative literature, and literary criticism. Through analysis, discussion, and criticism of literary works, the courses are designed to help students develop independent thinking, increase their understanding of Anglo-American literature and culture, nurture their cultural sensitivity in the humanities, and broaden their minds and vision. We hope to help students develop good understanding and problem-solving skills, capable of adapting to social changes in life and career. </span></span></span></p>
                        <p class="western" align="justify">&nbsp;</p>
                        <p><span style="color: #333333;"><strong>The Linguistics courses</strong></span>include phonetics, introduction to linguistics, pronunciation, sociolinguistics, psycholinguistics, and others. These courses aim at introducing a basic knowledge of English phonetics (e.g. the pronunciation, description, classification, variations, and phonological features of the English language), exploring the relationship between language and society (e.g. multilingual society, language and gender, language contact, and functions of language in different ethnic groups or societies), increasing students’ understanding of the basic issues in psycholinguistics (such as language acquisition, production, comprehension, language and the brain, and so forth), and discussing research methods and the fundamental spirit of linguistics. </span></span></span></p>
                        <p><span style="color: #333333;"><strong>The Applied English courses</strong></span> consist of foreign language acquisition theories, cultural studies and foreign language learning, English teaching methodology, macro strategies for English teaching, computer-assisted teaching, translation, practical English, business English, speech and debate, journalistic English, and Chinese-English interpretation. One part of the courses introduces students to the main issues in second-language acquisition and the importance of culture and society to language learning, helps them understand the principles, methods, and guidelines of English teaching, and explores the applications of computing technology in language teaching. The other part of the courses is designed to equip students with basic skills in practical writing, translation, business writing, communication, journalistic English, and Chinese-English interpretation.</span></span></span></p>
