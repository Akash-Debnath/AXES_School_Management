     <p>To provide a platform for the exploration of new paradigms in the philosophy of physical education, to extend expertise in specialized subject areas, to enhance the research oriented devices, to deal with social problems.

 

     </p> <strong>Objectives of the Program</strong>


  <p>The objectives of Master of Physical Education (M PEd) program are based on the departmental vision andmission. The program has been designed to meet the needs of working professionals who want to advancetheir careers by mastering physical education in Bangladesh and a pace that works for them. The students under this program will have the opportunity to take courses in a wide variety of timely and challenging practice areas of physical education. The general objective of this program is to offer students who already graduate on physical education. Other objectives of this program are as follows:


 (i) To provide a comprehensive sports knowledge.
 (ii) To develop a specialized knowledge of diverse learners.
 (iii) To introduce new dimension of technique and tactic of the contemporary sports world.
 (iv) To educate current and future physical instructors with a much-needed area of advanced academic training whose work will help to shape the society.
 (v) To provide in-depth training on the foundations of the contemporary principles and issues on physical education.
 (vi) To develop students’ ability to think critically, analyze arguments, articulate ideas and research efficiently.
 (vii) To develop advance academic techniques with comparative study to take present and future challenges.
 (viii) To demonstrate effective verbal, nonverbal, and media communication techniques to enhance learning and engagement in physical activity settings.
 (ix) To enable students to develop their inter personal skills.
 (x) To provide knowledge about planning, contemporary curriculum and assessment policy and practice, school contexts, and educational reform in the Bangladesh context.
 (xi) To demonstrate the key principles, concepts, and skills of physical education and knowledge of contemporary developments in their subject matter.

 <strong>Admission Requirements</strong>
    Students having Bachelor of Physical Education degree (Hons’ or pass) from any recognized institution or
    university shall be eligible to admit into this program. The applicant must have 2nd division or GPA 2.5 in BPEd
    degree. This requirement may be changed time to time by the rules of UGC and Admission Committee of this
    university.

    Teaching Strategy
    Medium of Instruction
    The medium of instruction and answer in the examination shall be English or Bangla. But the academic
    committee will have the option to decide what will be the medium of instruction and answer in the examination for each course.


    Duration of the Program

    The duration of this program shall be 4 (four) semesters in 02 (two) academic years. The duration of the program will be 01 (one) academic year (last 2 semesters) for the students who have completed honours degreein Physical Education from any recognized university or institution. Each semester will be 6 (six) months consisting of 18 (eighteen) weeks of which 13 (thirteen) weeks for class teaching, at least 1 (one) week as break and 4 (four) weeks for holding the semester final examination. For each course, there will be 1 (one) lecture of 60 minutes for each credit in every week & minimum 06 (six) hours for tutorial/open academic discussions in every semester. In case of practical course two class hours in a week will be considered as one credit. Students shall sit for the comprehensive viva-voce at the end of final semester of each year.


    Courses, Marks and Credit hours
    The program shall consist of 63 credits and shall be evaluated on the basis of 2100 total marks for 20 teaching
    credit courses units and 100 marks for oral examination. In case of BPEd honours, students will get 30 credits
    rebate from the total credit hours.

